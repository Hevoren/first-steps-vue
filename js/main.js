let app = new Vue({
    el: '#app',
    data: {
        premium: true,
        cart: [],
    },
    methods: {
        updateCart(id) {
            this.cart.push(id);
        },

        removeToCart(id) {
            this.cart.pop(id);

        }
    }
})




